namespace PatienceOS.Kernel
{
    /// <summary>
    /// Allowable terminal colors
    /// </summary>
    /// <remarks>
    /// List of colours sourced from here: <see cref="https://doc.ecoscentric.com/ref/framebuf-colour.html"/>
    /// </remarks>
    public enum Color
    {
        Black = 0x00,
        Blue = 0x01,
        Green = 0x02,
        Cyan = 0x03,
        Red = 0x04,
        Magenta = 0x05,
        Brown = 0x06,
        LightGrey = 0x07,
        DarkGrey = 0x08,
        LightBlue = 0x09,
        LightGreen = 0x0A,
        LightCyan = 0x0B,
        LightRed = 0x0C,
        LightMagenta = 0x0D,
        Yellow = 0x0E,
        White = 0x0F,
    }
}
